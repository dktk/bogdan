 <br /><h4>CAT TIMP ESTE VALABIL UN CERTIFICAT ENERGETIC ?</h4>

<p>Certificatul se elaborează şi se eliberează de către auditorul energetic pentru clădiri, la solicitarea investitorului/ proprietarului/administratorului clădirii/unităţii de clădire şi este valabil 10 ani de la data eliberării înscrisă în certificat, cu excepţia situaţiei în care, pentru clădirea/unitatea de clădire la care există certificat în valabilitate, se efectuează lucrări de renovare majoră care modifică consumurile energetice ale acesteia.</p>


  <br /><h4>CINE POATE SA INTOMEASCA UN CERTIFICAT ENERGETIC ?</h4>

<p>Certificatul de performanta energetica se realizeaza de catre un auditor energetic pentru clădiri care este o persoană fizică atestată de Ministerul Dezvoltării Regionale şi Administraţiei Publice, în conformitate cu prevederile legale în vigoare, care are dreptul să realizeze auditul energetic pentru clădiri/unităţi de clădire şi să întocmească certificatul de performanţă energetică şi raportul de audit energetic. Auditorul energetic pentru clădiri este specialistul care îşi desfăşoară activitatea ca persoană fizică autorizată sau ca angajat al unor persoane juridice, conform prevederilor legale în vigoare.
</p>


