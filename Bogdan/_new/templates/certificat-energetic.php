<br /><h4>INFORMATII SUPLIMENTARE DESPRE CERTIFICATUL ENERGETIC</h4>

Certificatul energetic se intocmeste conform Legii 372 republicata pentru urmatoarele categorii de cladiri, atât pentru clădirile noi, cât şi pentru clădirile existente, după cum urmează:
<ul class="cate">
<li> locuinţe unifamiliale
</li><li> blocuri de locuinţe
</li><li> birouri
</li><li> clădiri de învăţământ
</li><li> spitale
</li><li> hoteluri şi restaurante
</li><li> construcţii destinate activităţilor sportive
</li><li> clădiri pentru servicii de comerţ
</li><li> alte tipuri de clădiri consumatoare de energie
</li></ul>

<br /><h4>PENTRU CE CLADIRI NU SE FACE CERTIFICATUL ENERGETIC ?</h4>

Conform Legii 372 republicata certificatul energetic nu se face pentru urmatoarele categorii de cladiri:

<ul class="cate">
<li> clădiri şi monumente protejate care fie fac parte din zone construite protejate, conform legii, fie au valoare arhitecturală sau istorică deosebită, cărora, dacă li s-ar aplica cerinţele, li s-ar modifica în mod inacceptabil caracterul ori aspectul exterior
</li><li> clădiri utilizate ca lăcaşuri de cult sau pentru alte activităţi cu caracter religios
</li><li> clădiri provizorii prevăzute a fi utilizate pe perioade de până la 2 ani, din zone industriale, ateliere şi clădiri nerezidenţiale din domeniul agricol care necesită un consum redus de energie
</li><li> clădiri rezidenţiale care sunt destinate a fi utilizate mai puţin de 4 luni pe an
</li><li> clădiri independente, cu o suprafaţă utilă mai mică de 50 m²
</li></ul>

