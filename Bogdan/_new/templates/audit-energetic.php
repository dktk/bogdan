           <h2 class="font-normal margin-bottom-30">CERTIFICAT <span class="primary-color"> ENERGETIC</span></h2>
		   
		   <h4>PENTRU CE CLADIRI NU SE FACE AUDIT ENERGETIC ?</h4>

			 <p>Este important sa se faca DIFERENTA intre auditul energetic si certificatul energetic al unei cladiri. 
</p><p>Daca CERTIFICATUL ENERGETIC reprezinta un document prin care este indicată performanţa energetică a unei clădiri sau a unei unităţi de clădire din punct de vedere al consumurilor de energie, AUDITUL ENERGETIC vine si cu solutii pentru imbunatatirea performantei energetice a acesteia. </p>

<p>
<strong> Auditul energetic </strong> al clădirii reprezinta totalitatea  activităţilor specifice prin care se obţin date corespunzătoare despre profilul consumului energetic existent al unei clădiri/unităţi de clădire şi, după caz, de identificare şi de cuantificare a oportunităţilor rentabile de economisire a energiei prin identificarea soluţiilor de creştere a performanţei energetice, de cuantificare a economiilor de energie şi de evaluare a eficienţei economice a soluţiilor propuse cu estimarea costurilor şi a duratei de recuperare a investiţiei, precum şi de elaborare a raportului de audit energetic;
</p>

<p>
<strong>Raport de audit energetic </strong> este documentul elaborat în urma desfăşurării activităţii de audit energetic al clădirii, care conţine descrierea modului în care a fost efectuat auditul energetic, a principalelor caracteristici termice şi energetice ale clădirii/unităţii de clădire şi, acolo unde este cazul, a măsurilor propuse pentru creşterea performanţei energetice a clădirii/unităţii de clădire şi instalaţiilor interioare aferente acesteia, precum şi a principalelor concluzii referitoare la eficienţa economică a aplicării măsurilor propuse şi durata de recuperare a investiţiei;
</p>

</br>
 <h4>CARE ESTE SCOPUL ?</h4>

<p>
Scopul principal in realizarea auditului energetic este identificarea de masuri pentru reabilitarea si modernizarea energetica a cladirii pentru a  reduce consumurile de energie pentru incalzire, apa calda menajera, iluminat, ventilare si climatizare si asigurarea unui microclimat confortabil. O data cu reducerea consumurilor de energie se reduc implicit si emisiile de gaze poluante rezultate iar cladirea trece intr-o clasa energetica superioara.
</p>

<p>
Blocurile si cladirile care se reabiliteaza din punct de vedere energetic au obligatoriu nevoie de un audit energetic.
</p>

</br>
<h4>CARE SUNT PASII PENTRU ELABORAREA UNUI AUDIT ENERGETIC ?</h4>


		<p>
		Pasii care trebuie urmati pentru obtinerea auditului energetic sunt urmatorii :
		</p>

		<ul>
			<li>	Pentru inceput  se vor determina consumurile reale de energie ale cladirii, in conditiile normale de functionare. Pe baza rezultatelor se va realiza un raport de expertiza.
			</li><li>Se stabilesc masurile de modernizare energetica a cladirii; Acestea va trebui sa fie analizate si din punct de vedere economic pentru a alege varianta optima din punct de vedere al raportului pret / amortizare investitie in timp. Masurile de reducere a consumului energetic vor afecta atat anvelopa cat si instalatiile cladirii care fac obiectul auditului energetic si vor fi grupate in pachete de solutii.

			</li><li>Se intocmeste raportul de audit energetic, care este definit mai sus.
			</li>
		</ul>

		<br />
		<h4>CINE POATE SA FACA UN AUDIT ENERGETIC ?</h4>
		
		<p>Trebuie de mentionat faptul ca auditul energetic se poate realiza doar de catre auditori energetici gradul I atestati de catre Ministerul Dezvoltarii Regionale, Administratiei Publice si Fondurilor Europene.</p>



